function getOverrides(): Record<string, string> {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY) || '{}');
  } catch {
    return {};
  }
}

export function getContent(key: string): string {
  const overrides = getOverrides();
  return overrides[key] ?? DEFAULTS[key] ?? '';
}

export function getNumContent(key: string): number {
  return parseFloat(getContent(key)) || 0;
}

export function setContent(key: string, value: string): void {
  const overrides = getOverrides();
  if (value === DEFAULTS[key]) {
    delete overrides[key];
  } else {
    overrides[key] = value;
  }
  localStorage.setItem(STORAGE_KEY, JSON.stringify(overrides));
}

export function resetContent(key: string): void {
  const overrides = getOverrides();
  delete overrides[key];
  localStorage.setItem(STORAGE_KEY, JSON.stringify(overrides));
}

export function resetAll(): void {
  localStorage.removeItem(STORAGE_KEY);
}

export function getAllOverrides(): Record<string, string> {
  return getOverrides();
}

export function getDefault(key: string): string {
  return DEFAULTS[key] ?? '';
}

